#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*q;

	i = 0;
	q = (unsigned char *)s;
	while (i < n)
	{
		q[i] = 0;
		i++;
	}
}
